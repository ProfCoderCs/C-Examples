#include <iostream>

using namespace std;

int main()
{

    int number1;
    int number2;

    cout << "Enter two integer numbers: ";
    cin >> number1 >> number2;

    cout <<"Total is " << number1 + number2 << endl;
    cout <<"Subtraction is " << number1 - number2 << endl;
    cout <<"Multiplication is " << number1 * number2 << endl;
    cout <<"Division is " << number1 / number2 << endl;


    return 0;
}